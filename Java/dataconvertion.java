public class dataconvertion{
    public static void main(){
        //implicit
        int age = 18;
        double marks = age;
        
        System.out.println(marks);

        //explicit converstion
        double exp = 99.984;
        int exp1 = (int)exp;
        char exp2 = (char)exp;
        System.out.println(exp1);
        System.out.println(exp2);


    }
}
