public class datatype{
    public static void main(){
        int age = 18;
        double marks = 85.065;
        char a = 'a';
        boolean b = true;
        System.out.println("age ="+age);
        System.out.println("marks= "+ marks);
        System.out.println("char= "+ a);
        System.out.println("boolean= "+ b);


    }
}



// age =18
// marks=85.065
// char=a
// boolean=true